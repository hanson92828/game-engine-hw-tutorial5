float* matrix_add(float a[], float b[]) {
	float* c = new float[16];
	for (int i = 0; i < 16; i++) {
		c[i] = a[i] + b[i];
	}
	return c;
}

float* matrix_substract(float a[], float b[]) {
	float* c = new float[16];
	for (int i = 0; i < 16; i++) {
		c[i] = a[i] - b[i];
	}
	return c;
}

